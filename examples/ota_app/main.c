void ota_flash_state_machine(void)
{

  volatile int ret = 0;
  volatile uint8_t u8Cmd_Decoder = Ota_Dtat_Packet.ota_u8uart_read_buf[0]; // position 0 represents commnad from the tool

  switch (u8Cmd_Decoder)
  {
  case COMMAND_SET_INIT:
    ota_get_value_init_response();
    break;

  case COMMAND_WRITE_BINARY_DATA:
    ota_write_data_into_flash_response();
    break;

  case COMMAND_WRITE_PADDING_DATA:
    ota_write_padding_data_into_flash_response();
    break;

  case COMMAND_SEND_CRC:
    ota_crc_consistency_check_response();
    break;

  case COMMAND_APP_LOAD:
    ota_app_loading_request_response();
    break;

  case COMMAND_APP_ERASE:
    ota_erase_loaded_app_response();
    break;

  default:
    break;
  }

  // set call back for reading next uart data
  ret = ota_uart_data_read_cb_entry(UART_READ_BUFFER_SIZE);
  if (ret < 0)
  {
    printf("\tConsole Read Data Call Back Setting Failure!\n");
  }
}

