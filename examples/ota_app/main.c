bool ota_crc_consistency_check(stOtaDataPkt *pDataPacket)
{
  volatile bool boErr = false;

  // 1. finalize crc32 from tool
  uint32_t u32crc_ota_final = pDataPacket->ota_u32crc ^ 0xffffffff;

  // 2. calculate crc32 from ota to flash
  volatile uint32_t u32crc_from_tool = (pDataPacket->ota_u8uart_read_buf[1] << 24);
  u32crc_from_tool |= (pDataPacket->ota_u8uart_read_buf[2] << 16);
  u32crc_from_tool |= (pDataPacket->ota_u8uart_read_buf[3] << 8);
  u32crc_from_tool |= pDataPacket->ota_u8uart_read_buf[4];

  // 3. request to calculate crc32 of flashed data and receive it
  volatile int ret = ota_calculate_crc32(&pDataPacket->ota_u32Crc32_kernel);
  if (ret < 0)
  {
    printf("\tCRC32 system call error!\n");
  }

  //  4. crc consistency check
  if ((u32crc_ota_final == u32crc_from_tool) && (u32crc_ota_final == pDataPacket->ota_u32Crc32_kernel))
  {
    pDataPacket->ota_u32crc = 0; // Initialization for calculation of next update
    boErr = false;
  }
  else
  {
    boErr = true;
  }

  return boErr;
}

bool ota_erase_loaded_app(stOtaDataPkt *pDataPacket)
{
  int ret = 0;
  bool boErr = false;
  uint32_t u32FlashOffset = 0;

  memset(&pDataPacket->ota_u8flash_write_buf[0], 0x01, FLASH_BUFFER_SIZE); // set to value 1

  for (uint32_t u32idx = 0; u32idx < (pDataPacket->ota_u32recv_app_size / FLASH_BUFFER_SIZE); u32idx++)
  {
    pDataPacket->ota_boDone = false;

    u32FlashOffset = (pDataPacket->ota_u32flash_start_address_dyn - pDataPacket->ota_u32rom_start_address_const); // ex: 44000 - 44000
    u32FlashOffset += (u32idx * FLASH_BUFFER_SIZE);

    ret = ota_data_write_execution(u32FlashOffset, FLASH_BUFFER_SIZE);
    if (ret != 0)
    {
      boErr = true;
    }
    yield_for(&pDataPacket->ota_boDone);
  }

  return boErr;
}

