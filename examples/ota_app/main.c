void ota_blink_binary_write(void)
{
  int ret;
  uint32_t u32arry_len = sizeof(blkapp_bin) / sizeof(uint8_t);
  uint8_t writebuf[FLASH_BUFFER_SIZE];
  uint32_t u32start_address = 0;
  uint32_t u32Rom_end_address = 0;
  uint32_t u32FlashOffset = 0;
  uint32_t u32process_index = 0;
  uint32_t u32Process_max_index = 0;
  uint32_t u32Crc32_kernel = 0;
  uint32_t u32crc = 0;

  // 0. Need to implement Permission process to access Nonvolatile_storate_driver and Process_load_utilities
  // To do..

  // 1. Find the start address of writable flash to install an application
  ret = ota_set_dynamic_start_address_of_writable_flash();
  if (ret != 0)
  {
    printf("\tERROR calling read\n");
    // return ret;
  }

  // 2. Get the index of process that we will write
  ret = ota_get_process_index(&u32process_index);
  if (ret < 0)
  {
    printf("\tError getting process index\n");
  }
  printf("\tProc index at OTA: %ld\n", u32process_index);

  // 3. Get the start address of writable flash
  ret = ota_get_dynamic_start_address_of_writable_flash(&u32start_address);
  if (ret < 0)
  {
    printf("\tError getting dynamic start address of writable flash\n");
  }
  printf("\tstart address at OTA: 0x%lx\n", u32start_address);

  // 4. Get the end address of writable flash
  ret = ota_get_end_address_of_writable_flash(&u32Rom_end_address);
  if (ret < 0)
  {
    printf("Error getting rom end address\n");
  }
  printf("\tROM end address at OTA: 0x%lx\n", u32Rom_end_address);

  // 5. Get the maximum number of process supported by platform
  ret = ota_get_process_maxnum(&u32Process_max_index);
  if (ret < 0)
  {
    printf("Error getting process max index\n");
  }
  printf("\tProcess max index at OTA: %ld\n", u32Process_max_index);

  // 4. Check the number of processes and the size of writable flash region. If this condition is not satisfied, skip writing application procedure
  if ((u32process_index < u32Process_max_index) && ((u32Rom_end_address - u32start_address) > sizeof(blkapp_bin)))
  {
    ret = ota_flash_internal_write_buffer(writebuf, FLASH_BUFFER_SIZE);
    if (ret != RETURNCODE_SUCCESS)
    {
      printf("\tERROR setting write buffer\n");
    }

    // 5. write binary code into flash
    for (uint32_t u32offset = 0; u32offset < 5; u32offset++)
    {
      if (u32offset == 4)
      {
        // 5.1 write dummy binary code into flash
        memset(writebuf, 0x01, FLASH_BUFFER_SIZE);
      }
      else
      {
        memcpy(writebuf, &blkapp_bin[FLASH_BUFFER_SIZE * u32offset], FLASH_BUFFER_SIZE);

        // 5.1 CRC32-POXIS (init: 0x00000000, poly: 0x04c11db7)
        for (uint16_t i = 0; i < FLASH_BUFFER_SIZE; i++)
        {
          u32crc = (u32crc << 8) ^ crc32_posix_lookup_table[((u32crc >> 24) ^ writebuf[i]) & 0xFF];
        }
      }

      done = false;
      u32FlashOffset = (u32offset * FLASH_BUFFER_SIZE) + (u32start_address - Ota_Dtat_Packet.ota_u32rom_start_address_const);
      printf("\tFlashOffset at OTA: 0x%lx\n", u32FlashOffset);

      ret = ota_data_write_execution(u32FlashOffset, FLASH_BUFFER_SIZE);
      if (ret != 0)
      {
        printf("\tERROR calling write execution\n");
      }
      yield_for(&done);
    }

    // 5.2 Finalize
    u32crc ^= 0xffffffff;

    // 6. request to calculate crc32 and return the resulting value
    ret = ota_calculate_crc32(&u32Crc32_kernel);
    if (ret < 0)
    {
      printf("CRC32 calculation error\n");
    }
    printf("\tCRC32 Result at App layer: %ld\n", u32crc);
    printf("\tCRC32 Result at Kernel Layer: %ld\n", u32Crc32_kernel);

    // 7.Check CRC consistency between data sent and data flashed
    if (u32crc == u32Crc32_kernel)
    {
      ret = ota_app_loading_request();
      if (ret != 0)
      {
        printf("\tERROR calling app loading request\n");
      }
      printf("\tLoading!\n");
    }
    else
    {
      // Erase the falsh region to 0x01
      for (uint32_t u32offset = 0; u32offset < u32arry_len; u32offset += FLASH_BUFFER_SIZE)
      {
        memset(writebuf, 0x01, FLASH_BUFFER_SIZE); // set to value 1
        done = false;
        u32FlashOffset = u32offset + (u32start_address - Ota_Dtat_Packet.ota_u32rom_start_address_const);
        ret = ota_data_write_execution(u32FlashOffset, FLASH_BUFFER_SIZE);
        if (ret != 0)
        {
          printf("\tERROR calling write\n");
        }
        yield_for(&done);
      }
      printf("\tErase!\n");
    }
  }
  else
  {
    printf("\tApplications cannot be updated more than 4\n");
    printf("\tFlash region is not enough to load the new application\n");
  }
}

